-- TABELA: policiais
create table policiais (
  id bigint generated by default as identity primary key,
  nome text not null,
  matricula text not null,
  patente text not null,
  secao text default '',
  created_at timestamptz default now()
);

-- TABELA: gestores
create table gestores (
  id bigint generated by default as identity primary key,
  nome text not null,
  matricula text not null,
  senha text not null,
  principal boolean default false,
  created_at timestamptz default now()
);

-- TABELA: solicitacoes
create table solicitacoes (
  id bigint generated by default as identity primary key,
  policial_id bigint references policiais(id),
  policial_nome text,
  matricula text,
  patente text,
  secao text,
  dia text,
  semana text,
  motivo text,
  status text default 'pendente',
  created_at timestamptz default now()
);

-- =============================================
-- DESABILITAR RLS (acesso público simplificado)
-- =============================================
alter table policiais disable row level security;
alter table gestores disable row level security;
alter table solicitacoes disable row level security;

